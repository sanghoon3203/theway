import SwiftUI

struct AchievementListView: View {
    @StateObject private var viewModel = AchievementViewModel()
    @State private var selectedCategory: AchievementCategory? = nil
    @State private var showCompletedOnly = false
    
    var body: some View {
        NavigationView {
            ZStack {
                LinearGradient.oceanWave
                    .ignoresSafeArea()
                
                VStack(spacing: 0) {
                    // ÌïÑÌÑ∞ ÏÑπÏÖò
                    filterSection
                    
                    // ÏóÖÏ†Å Î™©Î°ù
                    achievementsList
                }
            }
            .navigationTitle("ÏóÖÏ†Å")
            .navigationBarTitleDisplayMode(.large)
            .onAppear {
                viewModel.loadAchievements()
            }
            .alert("ÏóÖÏ†Å ÏôÑÎ£å!", isPresented: $viewModel.showCompletionAlert) {
                Button("ÌôïÏù∏", role: .cancel) { }
            } message: {
                if let newAchievement = viewModel.newlyCompletedAchievement {
                    Text("'\(newAchievement.name)' ÏóÖÏ†ÅÏùÑ Îã¨ÏÑ±ÌñàÏäµÎãàÎã§!\n\(newAchievement.rewardInfo?.displayText ?? "")")
                }
            }
        }
    }
    
    // MARK: - ÌïÑÌÑ∞ ÏÑπÏÖò
    private var filterSection: some View {
        VStack(spacing: 12) {
            // Ïπ¥ÌÖåÍ≥†Î¶¨ ÌïÑÌÑ∞
            ScrollView(.horizontal, showsIndicators: false) {
                HStack(spacing: 12) {
                    CategoryFilterButton(
                        title: "Ï†ÑÏ≤¥",
                        isSelected: selectedCategory == nil,
                        action: { selectedCategory = nil }
                    )
                    
                    ForEach(AchievementCategory.allCases, id: \.rawValue) { category in
                        CategoryFilterButton(
                            title: category.displayName,
                            icon: category.icon,
                            color: category.color,
                            isSelected: selectedCategory == category,
                            action: { selectedCategory = category }
                        )
                    }
                }
                .padding(.horizontal)
            }
            
            // ÏôÑÎ£åÎêú ÏóÖÏ†ÅÎßå Î≥¥Í∏∞ ÌÜ†Í∏Ä
            HStack {
                Toggle("ÏôÑÎ£åÎêú ÏóÖÏ†ÅÎßå Î≥¥Í∏∞", isOn: $showCompletedOnly)
                    .font(.merchantBody)
                    .foregroundColor(.dialogueText)
                Spacer()
            }
            .padding(.horizontal)
        }
        .padding(.vertical)
        .background(
            RoundedRectangle(cornerRadius: 15)
                .fill(LinearGradient.parchmentGlow)
                .overlay(
                    RoundedRectangle(cornerRadius: 15)
                        .stroke(Color.dialogueBorder, lineWidth: 1)
                )
        )
        .padding(.horizontal)
    }
    
    // MARK: - ÏóÖÏ†Å Î™©Î°ù
    private var achievementsList: some View {
        ScrollView {
            LazyVStack(spacing: 12) {
                ForEach(filteredAchievements) { achievement in
                    AchievementCard(
                        achievement: achievement,
                        onClaimReward: { achievementId in
                            viewModel.claimReward(for: achievementId)
                        }
                    )
                }
                
                if filteredAchievements.isEmpty {
                    EmptyAchievementsView()
                }
            }
            .padding()
        }
    }
    
    // MARK: - Í≥ÑÏÇ∞Îêú ÏÜçÏÑ±
    private var filteredAchievements: [Achievement] {
        viewModel.achievements.filter { achievement in
            // Ïπ¥ÌÖåÍ≥†Î¶¨ ÌïÑÌÑ∞
            if let selectedCategory = selectedCategory,
               achievement.category != selectedCategory {
                return false
            }
            
            // ÏôÑÎ£å ÏÉÅÌÉú ÌïÑÌÑ∞
            if showCompletedOnly && !achievement.isCompleted {
                return false
            }
            
            return true
        }
    }
}

// MARK: - CategoryFilterButton Ïª¥Ìè¨ÎÑåÌä∏
struct CategoryFilterButton: View {
    let title: String
    let icon: String?
    let color: Color?
    let isSelected: Bool
    let action: () -> Void
    
    init(
        title: String,
        icon: String? = nil,
        color: Color? = nil,
        isSelected: Bool,
        action: @escaping () -> Void
    ) {
        self.title = title
        self.icon = icon
        self.color = color
        self.isSelected = isSelected
        self.action = action
    }
    
    var body: some View {
        Button(action: action) {
            HStack(spacing: 6) {
                if let icon = icon {
                    Image(systemName: icon)
                        .font(.system(size: 14))
                }
                
                Text(title)
                    .font(.compassSmall)
            }
            .padding(.horizontal, 12)
            .padding(.vertical, 8)
            .background(
                RoundedRectangle(cornerRadius: 20)
                    .fill(isSelected ? (color ?? .compass) : Color.parchmentBase)
                    .overlay(
                        RoundedRectangle(cornerRadius: 20)
                            .stroke(isSelected ? (color ?? .compass) : Color.dialogueBorder, lineWidth: 1)
                    )
            )
            .foregroundColor(isSelected ? .white : .dialogueText)
        }
        .animation(.spring(response: 0.3, dampingFraction: 0.7), value: isSelected)
    }
}

// MARK: - AchievementCard Ïª¥Ìè¨ÎÑåÌä∏
struct AchievementCard: View {
    let achievement: Achievement
    let onClaimReward: (String) -> Void
    
    var body: some View {
